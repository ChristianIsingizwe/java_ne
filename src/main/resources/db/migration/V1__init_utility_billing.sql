CREATE OR REPLACE FUNCTION set_updated_at()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
    NEW.updated_at = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION insert_bill_notification()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
    customer_name TEXT;
    bill_period TEXT;
BEGIN
    SELECT p.full_name
      INTO customer_name
      FROM customers c
      JOIN profiles p ON p.id = c.profile_id
     WHERE c.id = NEW.customer_id;

    bill_period := to_char(make_date(NEW.billing_year, NEW.billing_month, 1), 'FMMonth') || ' / ' || NEW.billing_year;

    INSERT INTO notifications (customer_id, bill_id, notification_type, message, status)
    VALUES (
        NEW.customer_id,
        NEW.id,
        'BILL_GENERATED',
        'Dear ' || customer_name || E'.\nYour ' || bill_period || ' utility bill of ' || NEW.total_amount || ' FRW has been successfully processed.',
        'PENDING'
    );
    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION apply_payment_approval()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
    approved_total NUMERIC(14, 2);
    bill_total NUMERIC(14, 2);
    bill_year INTEGER;
    bill_month INTEGER;
    customer_id_ref BIGINT;
    customer_name TEXT;
    bill_period TEXT;
BEGIN
    IF NEW.status <> 'APPROVED' OR (TG_OP = 'UPDATE' AND OLD.status = 'APPROVED') THEN
        RETURN NEW;
    END IF;

    SELECT COALESCE(SUM(amount_paid), 0.00)
      INTO approved_total
      FROM payments
     WHERE bill_id = NEW.bill_id
       AND status = 'APPROVED';

    SELECT total_amount, billing_year, billing_month, customer_id
      INTO bill_total, bill_year, bill_month, customer_id_ref
      FROM bills
     WHERE id = NEW.bill_id;

    UPDATE bills
       SET amount_paid = approved_total,
           outstanding_balance = GREATEST(total_amount - approved_total, 0.00),
           status = CASE
               WHEN approved_total >= total_amount THEN 'PAID'
               WHEN approved_total > 0 THEN 'PARTIALLY_PAID'
               ELSE status
           END
     WHERE id = NEW.bill_id;

    IF approved_total >= bill_total THEN
        SELECT p.full_name
          INTO customer_name
          FROM customers c
          JOIN profiles p ON p.id = c.profile_id
         WHERE c.id = customer_id_ref;

        bill_period := to_char(make_date(bill_year, bill_month, 1), 'FMMonth') || ' / ' || bill_year;

        INSERT INTO notifications (customer_id, bill_id, payment_id, notification_type, message, status)
        VALUES (
            customer_id_ref,
            NEW.bill_id,
            NEW.id,
            'PAYMENT_COMPLETED',
            'Dear ' || customer_name || E'.\nYour ' || bill_period || ' utility bill of ' || bill_total || ' FRW has been successfully processed.',
            'PENDING'
        );
    END IF;

    RETURN NEW;
END;
$$;

