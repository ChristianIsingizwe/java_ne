CREATE TABLE customers (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    profile_id  BIGINT    NOT NULL UNIQUE,
    created_at  TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at  TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_customers_profile FOREIGN KEY (profile_id) REFERENCES profiles (id)
);

CREATE TABLE meters (
    id                 BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_id        BIGINT      NOT NULL,
    meter_number       VARCHAR(50) NOT NULL UNIQUE,
    meter_type         VARCHAR(20) NOT NULL,
    billing_mode       VARCHAR(20) NOT NULL,
    installation_date  DATE        NOT NULL,
    status             VARCHAR(20) NOT NULL,
    created_at         TIMESTAMP   NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at         TIMESTAMP   NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_meters_customer FOREIGN KEY (customer_id) REFERENCES customers (id),
    CONSTRAINT chk_meters_type CHECK (meter_type IN ('WATER', 'ELECTRICITY')),
    CONSTRAINT chk_meters_billing_mode CHECK (billing_mode IN ('PREPAID', 'POSTPAID')),
    CONSTRAINT chk_meters_status CHECK (status IN ('ACTIVE', 'INACTIVE')),
    CONSTRAINT chk_water_postpaid CHECK (NOT (meter_type = 'WATER' AND billing_mode = 'PREPAID'))
);

CREATE TABLE meter_readings (
    id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    meter_id          BIGINT         NOT NULL,
    previous_reading  NUMERIC(14, 2) NOT NULL,
    current_reading   NUMERIC(14, 2) NOT NULL,
    reading_date      DATE           NOT NULL,
    reading_year      INTEGER        NOT NULL,
    reading_month     INTEGER        NOT NULL,
    created_at        TIMESTAMP      NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at        TIMESTAMP      NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_meter_readings_meter FOREIGN KEY (meter_id) REFERENCES meters (id),
    CONSTRAINT uq_meter_reading_period UNIQUE (meter_id, reading_year, reading_month),
    CONSTRAINT chk_meter_readings_values CHECK (current_reading > previous_reading),
    CONSTRAINT chk_meter_readings_month CHECK (reading_month BETWEEN 1 AND 12)
);

CREATE TABLE tariff_versions (
    id                         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    meter_type                 VARCHAR(20)   NOT NULL,
    tariff_type                VARCHAR(20)   NOT NULL,
    version_number             INTEGER       NOT NULL,
    effective_from             DATE          NOT NULL,
    fixed_service_charge       NUMERIC(14, 2) NOT NULL,
    vat_rate                   NUMERIC(5, 4)  NOT NULL,
    late_payment_penalty_rate  NUMERIC(5, 4)  NOT NULL,
    created_at                 TIMESTAMP     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at                 TIMESTAMP     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uq_tariff_versions UNIQUE (meter_type, effective_from, version_number),
    CONSTRAINT chk_tariff_meter_type CHECK (meter_type IN ('WATER', 'ELECTRICITY')),
    CONSTRAINT chk_tariff_type CHECK (tariff_type IN ('FLAT', 'TIERED')),
    CONSTRAINT chk_tariff_vat CHECK (vat_rate = 0.1800),
    CONSTRAINT chk_tariff_penalty CHECK (late_payment_penalty_rate >= 0)
);

CREATE TABLE tariff_tiers (
    id                 BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tariff_version_id  BIGINT          NOT NULL,
    from_unit          INTEGER         NOT NULL,
    to_unit            INTEGER,
    price_per_unit     NUMERIC(14, 2)  NOT NULL,
    created_at         TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at         TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_tariff_tiers_version FOREIGN KEY (tariff_version_id) REFERENCES tariff_versions (id),
    CONSTRAINT chk_tariff_tiers_bounds CHECK (to_unit IS NULL OR to_unit > from_unit)
);

CREATE TABLE bills (
    id                   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    bill_reference       VARCHAR(80)   NOT NULL UNIQUE,
    customer_id          BIGINT        NOT NULL,
    meter_id             BIGINT        NOT NULL,
    reading_id           BIGINT        NOT NULL UNIQUE,
    tariff_version_id    BIGINT        NOT NULL,
    billing_year         INTEGER       NOT NULL,
    billing_month        INTEGER       NOT NULL,
    due_date             DATE          NOT NULL,
    consumption_units    NUMERIC(14, 2) NOT NULL,
    subtotal             NUMERIC(14, 2) NOT NULL,
    vat_amount           NUMERIC(14, 2) NOT NULL,
    penalty_amount       NUMERIC(14, 2) NOT NULL,
    total_amount         NUMERIC(14, 2) NOT NULL,
    amount_paid          NUMERIC(14, 2) NOT NULL,
    outstanding_balance  NUMERIC(14, 2) NOT NULL,
    status               VARCHAR(30)   NOT NULL,
    generated_by         BIGINT,
    approved_by          BIGINT,
    approved_at          TIMESTAMP,
    created_at           TIMESTAMP     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at           TIMESTAMP     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_bills_customer FOREIGN KEY (customer_id) REFERENCES customers (id),
    CONSTRAINT fk_bills_meter FOREIGN KEY (meter_id) REFERENCES meters (id),
    CONSTRAINT fk_bills_reading FOREIGN KEY (reading_id) REFERENCES meter_readings (id),
    CONSTRAINT fk_bills_tariff FOREIGN KEY (tariff_version_id) REFERENCES tariff_versions (id),
